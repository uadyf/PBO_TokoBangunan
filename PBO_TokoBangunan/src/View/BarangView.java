    private void tombolHapusBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolHapusBarangActionPerformed
        // TODO add your handling code here:
        int row = tabelBarang.getSelectedRow();
        if (row >= 0) {
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus barang ini?");
            if (confirm == JOptionPane.YES_OPTION) {
                int idBarang = (int) tabelBarang.getValueAt(row, 0); // ambil ID dari kolom pertama
                barangController.hapusBarang(idBarang);
                loadDataBarang();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih salah satu barang yang ingin dihapus.");
        }
        loadDataBarang();
    }//GEN-LAST:event_tombolHapusBarangActionPerformed

    private void tombolEditBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolEditBarangActionPerformed
        int row = tabelBarang.getSelectedRow();
        if (row >= 0) {
            int idBarang = (int) tabelBarang.getValueAt(row, 0);
            Barang barang = barangController.cariById(idBarang);
            FormEdit formEdit = new FormEdit(); // form edit
            formEdit.setBarang(barang);
            formEdit.setVisible(true);

            loadDataBarang();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih barang yang ingin diedit.");
        }
        loadDataBarang();
    }//GEN-LAST:event_tombolEditBarangActionPerformed

