    private void loadDataBarang() {
        DefaultTableModel model = (DefaultTableModel) tabelBarang.getModel();
        model.setRowCount(0); // kosongkan isi tabel sebelum menambah data baru

        List<Barang> listBarang = barangController.tampilkanSemua();
        for (Barang b : listBarang) {
            Object[] row = {
                b.getIdBarang(), 
                b.getNamaBarang(), 
                b.getIdKategori(),
                b.getStok(), 
                b.getHargaBeli(),
                b.getHargaJual()
            };
            model.addRow(row);
        }
    }


