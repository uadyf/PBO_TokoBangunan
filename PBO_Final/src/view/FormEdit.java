    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNamaBarang.getText();
        kategori kategori = (kategori) comboKategori.getSelectedItem();
        String stokText = txtStok.getText();
        String hargaBeliText = txtHargaBeli.getText();
        String hargaJualText = txtHargaJual.getText();

        if (nama.isEmpty() || stokText.isEmpty() || hargaBeliText.isEmpty() || hargaJualText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi.");
            return;
        }

        try {
            int stok = Integer.parseInt(stokText);
            double hargaBeli = Double.parseDouble(hargaBeliText);
            double hargaJual = Double.parseDouble(hargaJualText);

            barang barang = new barang();
            barang.setIdBarang(barang_default.getIdBarang());
            barang.setIdKategori(kategori.getId());
            barang.setNamaBarang(nama);
            barang.setNamaKategori(kategori.getNama());
            barang.setStok(stok);
            barang.setHargaBeli(hargaBeli);
            barang.setHargaJual(hargaJual);

            barangCont.updateBarang(barang);
            
            JOptionPane.showMessageDialog(this, "Barang berhasil diupdate.");
            dispose(); // tutup form setelah simpan

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Stok, harga beli, dan harga jual harus berupa angka.");
        }
    }//GEN-LAST:event_SimpanActionPerformed

