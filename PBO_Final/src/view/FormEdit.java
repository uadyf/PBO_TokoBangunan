    public void setBarangDefault(barang b) {
        this.barang_default = b;
        
        txtNamaBarang.setText(barang_default.getNamaBarang());
        for (int i = 0; i < comboKategori.getItemCount(); i++) {
            if (comboKategori.getItemAt(i).getId() == barang_default.getIdKategori()) {
                comboKategori.setSelectedIndex(i);
                break;
            }
        }
        txtStok.setText("" + barang_default.getStok());
        txtHargaBeli.setText("" + barang_default.getHargaBeli());
        txtHargaJual.setText("" + barang_default.getHargaJual());
        
    }

